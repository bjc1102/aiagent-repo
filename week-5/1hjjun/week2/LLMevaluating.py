def run_llm_evaluation():
    SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
    dataset_path = os.path.join(SCRIPT_DIR, "../golden_dataset.json")
    output_path = os.path.join(SCRIPT_DIR, "LLMresult.json")

    if not os.path.exists(dataset_path):
        print(f"⚠️ {dataset_path} 파일을 찾을 수 없습니다.")
        return

    with open(dataset_path, "r", encoding="utf-8") as f:
        golden_dataset = json.load(f)

    total = len(golden_dataset)
    print(f"\n🎯 총 {total}개 질문에 대한 ContextLLM 평가를 시작합니다\n")

    results = []

    for idx, item in enumerate(golden_dataset):
        q_id = item["id"]
        question = item["question"]
        expected_answer = item["expected_answer"]
        difficulty = item.get("difficulty", "unknown")
        source_year = item.get("source_year", "")

        print(f"[{idx+1}/{total}] {q_id} ({difficulty}) 진행 중...")

        generated_answer = generate_answer(question)

        results.append({
            "id": q_id,
            "difficulty": difficulty,
            "source_year": source_year,
            "question": question,
            "expected_answer": expected_answer,
            "generated_answer": generated_answer,
        })

        print(f"   → 완료")

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=4)

    print(f"\n✅ 완료! 상세 결과가 '{output_path}'에 저장되었습니다.")
    print(f"   총 {total}개 질문 처리 완료.")


if __name__ == "__main__":
    run_llm_evaluation()
