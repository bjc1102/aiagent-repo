from pathlib import Path
import json

BASE_DIR = Path(__file__).resolve().parent


def load_copayment_reference(file_path: str = "copayment_reference.txt") -> str:
    """
    copayment_reference.txt 파일을 읽어서 문자열로 반환합니다.
    """
    full_path = BASE_DIR / file_path
    return full_path.read_text(encoding="utf-8").strip()


def load_json_reference(file_path: str = "copayment_reference.json") -> str:
    """
    copayment_reference.json 파일을 읽어서 pretty JSON 문자열로 반환합니다.
    """
    full_path = BASE_DIR / file_path
    with open(full_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    return json.dumps(data, ensure_ascii=False, indent=2)


def get_step1_prompt() -> str:
    """
    Step 1: Zero-shot Baseline
    - 참조 데이터만 제공
    - 예시는 넣지 않음
    """
    reference = load_copayment_reference()

    prompt = f"""
당신은 의료급여 본인부담률 계산을 정확하게 답하는 도우미입니다.

아래 참조 데이터만 사용해서 질문에 답하세요.

[참조 데이터]
{reference}

[지시사항]
1. 질문에 나온 조건을 순서대로 확인하세요.
   - 수급권자 종별
   - 나이
   - 입원/외래
   - 의료기관 종류
   - 질환 여부
   - 임신 여부
   - 기타 예외 조건
2. 일반 규칙과 예외 규칙이 동시에 보이면, 더 구체적인 예외 규칙을 우선하세요.
3. 반드시 최종 답 하나만 선택하세요.
4. 출력은 반드시 JSON 객체 하나만 작성하세요.
5. JSON 바깥의 설명, 문장, 코드블록, 마크다운은 절대 출력하지 마세요.

[출력 형식]
{{
  "answer": "정답값",
  "reason": "어떤 조건을 적용했는지 한두 문장으로 간단히 설명"
}}
""".strip()

    return prompt


def get_step2_prompt() -> str:
    """
    Step 2: Few-shot Prompting
    """
    reference = load_copayment_reference()

    prompt = f"""
당신은 의료급여 본인부담률 계산을 정확하게 수행하는 전문가입니다.

반드시 아래 참조 데이터만 사용해서 질문에 답하세요.

[참조 데이터]
{reference}

========================
[예시 1 - 퍼센트 문제]
질문:
65세 이상 1종 수급권자가 틀니를 하면 본인부담률은 몇 %인가요?

정답:
{{
  "answer": "5%",
  "reason": "65세 이상 1종 수급권자의 틀니는 5%"
}}

========================
[예시 2 - 금액 계산 문제]
질문:
65세 이상 1종 수급권자가 틀니 시술을 받았고 시술비가 1,200,000원입니다. 본인부담금은 얼마인가요?

정답:
{{
  "answer": "60,000원",
  "reason": "틀니 5%를 적용하면 1,200,000 × 0.05 = 60,000원"
}}

========================
[예시 3 - 조건 포함 출력 문제]
질문:
2종 수급권자의 조현병 외 정신질환 환자가 외래 진료를 받으면 본인부담률은 얼마인가요?

정답:
{{
  "answer": "병원급이상10%",
  "reason": "조현병 외 정신질환 외래진료는 병원급 이상 10%"
}}

========================
[예시 4 - 치아 홈메우기 특례 문제]
질문:
2종 수급권자인 16세 청소년이 치아 홈메우기로 입원하여 진료비가 320,000원 발생했습니다. 본인부담금은 얼마인가요?

정답:
{{
  "answer": "16,000원",
  "reason": "치아 홈메우기 입원은 16세 이상~18세 이하 5%이므로 320,000 × 0.05 = 16,000원"
}}

========================
[예시 5 - CT/MRI/PET 문제]
질문:
2종 수급권자인 조현병 환자가 제2차의료급여기관에서 PET를 찍었고 검사비가 1,400,000원입니다. 본인부담금은 얼마인가요?

정답:
{{
  "answer": "210,000원",
  "reason": "2종 수급권자의 CT/MRI/PET 기본 규칙은 15%이므로 1,400,000 × 0.15 = 210,000원"
}}

========================
[예시 6 - 적용 여부 판단 문제]
질문:
65세 이상 2종 수급권자가 임플란트를 할 때 본인부담 보상제나 상한제가 적용되나요?

정답:
{{
  "answer": "해당되지않음",
  "reason": "임플란트는 본인부담 보상제·상한제 대상이 아님"
}}

========================

[핵심 규칙]

1. 질문 유형을 먼저 판단한다.
- "몇 %인가요?" → 퍼센트로 답한다
- "본인부담금은 얼마인가요?" → 반드시 최종 금액으로 계산해서 답한다
- "적용되나요?" → 해당 여부를 답한다

2. 금액 문제는 반드시 계산한다.
- 퍼센트 × 금액
- 반드시 계산된 최종 금액만 answer에 작성한다
- 금액 문제에서 퍼센트를 answer로 쓰면 오답이다

3. reason과 answer는 반드시 완전히 일치해야 한다.
- reason에서 계산한 최종값을 answer에 그대로 쓴다
- 예: reason이 "320,000 × 0.05 = 16,000원"이면 answer도 반드시 "16,000원"이어야 한다
- reason과 answer가 다르면 오답이다

4. 규칙 적용 순서는 반드시 아래와 같다.
- 항목별 특례(치아 홈메우기, 추나요법, CT/MRI/PET)
- 연령 특례(1세 미만, 6세 미만, 6세 이상~15세 이하, 16세 이상~18세 이하)
- 질환 특례(만성질환, 정신질환, 치매질환 등)
- 기본 규칙

5. 치아 홈메우기는 반드시 별도 특례 규칙을 먼저 적용한다.
- 6세 미만 → 무료
- 6세 이상~15세 이하 → 3%
- 16세 이상~18세 이하 → 5%

6. CT, MRI, PET는 반드시 별도 검사 규칙으로 판단한다.
- 일반 외래 규칙으로 먼저 계산하지 않는다
- 검사 항목 특례가 있으면 그 특례를 우선 적용한다

7. 1세 미만 관련 문항은 반드시 다시 확인한다.
- 1세 미만 + 제1차의료급여기관 외래 → 무료
- 1세 미만 + 제2·3차의료급여기관 외래 → 5%
- 1세 미만 만성질환자 + 제2차의료급여기관 → 참조 데이터의 해당 특례를 그대로 적용한다

8. 정신질환 외래진료 문항은 조건 포함 형식으로 출력한다.
- 조현병 → 병원급이상5%
- 조현병 외 정신질환 → 병원급이상10%
- "5%" 또는 "10%"만 쓰면 오답이다

9. 여러 항목이 있으면 각각 계산해서 함께 출력한다.
- 반드시 항목 이름 포함
- 예: 틀니75,000원,임플란트100,000원
- 예: 복잡추나60,000원,단순추나36,000원
- 쉼표(,)만 사용하고 슬래시(/)는 사용 금지

10. "해당되지않음"은 참조 데이터에 실제로 비해당이라고 나온 경우에만 사용한다.
- 정보 부족을 이유로 임의로 "해당되지않음"을 쓰지 않는다

11. answer 출력 형식은 아래 중 하나만 허용한다.
- 무료
- 숫자원
- 숫자%
- 병원급이상숫자%
- 항목명숫자원,항목명숫자원
- 해당되지않음

12. 아래 형식은 절대 금지한다.
- 설명 문장
- 퍼센트와 금액 혼합
- 항목명 없는 다중 금액
- 조건 없는 축약 답변
- reason과 다른 answer

13. 출력은 반드시 JSON 객체 하나만 작성한다.
- JSON 바깥의 설명, 문장, 코드블록, 마크다운은 절대 출력하지 않는다

[출력 형식]
{{
  "answer": "정답값",
  "reason": "간단한 설명"
}}
""".strip()

    return prompt


