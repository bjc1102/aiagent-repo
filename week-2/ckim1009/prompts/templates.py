def get_prompt(prompt_type:str, md_file_path):
    
    try:
        md_content = pathlib.Path(md_file_path).read_text(encoding="utf-8")
    except FileNotFoundError:
        md_content = "파일을 찾을 수 없습니다. 경로를 확인해주세요."

    prompt = ZERO_SHOT_PROMPT.format(md_content=md_content)
    
    prompt_type = prompt_type.lower()
    if prompt_type == 'few_shot':
        prompt = FEW_SHOT_PROMPT.format(md_content=md_content)
    elif prompt_type == 'cot':
        prompt = COT_PROMPT.format(md_content=md_content)
    elif prompt_type == 'self_consistency':
        prompt = SELF_CONSISTENCY_PROMPT.format(md_content=md_content)

    return prompt